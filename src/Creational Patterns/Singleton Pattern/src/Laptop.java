class Laptop {
    private String model;
    private double price;

    public Laptop(String model, double price) {  // Constructor to initialize model and price
        this.model = model;
        this.price = price;
    }

    public String getModel() {   // Get the laptop model name
        return model;
    }

    public double getPrice() {    // Get the laptop price
        return price;
    }
}



